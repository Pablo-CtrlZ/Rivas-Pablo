const A = "Hola";
let B = " mundo!";
document.write("Constante A contiene " + A);
document.write("</br>");
document.write("Variable B contiene " + B);
document.write("</br>");
document.write(A + B);
document.write("</br>");
document.write("La etiqueta BR es un salto de linea");
document.write("</br>");
document.write("El signo '+' sirve para concatenar cadenas de texto");
//Lo que hace document.write() es mostrar en el navegador (y no en la consola) un mensaje que viene dado por un string concatenado a una variable.
//La etiqueta </br> sirve para dar un salto de linea, mientras que el signo + sirve para concatenar strings.